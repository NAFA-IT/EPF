-- ── Seed REQUEST workflow statuses ────────────────────────────
-- Pattern follows EPF_STATUSES usage elsewhere (CATEGORY_CODE +
-- STATUS_CODE + STATUS_DISPLAY).  MERGE = idempotent: inserts
-- only if not already present.  Never reference IDs directly —
-- always EPF_STATUS_PKG.GET_ID('REQUEST', <code>).
MERGE INTO EPF_STATUSES s
USING (
    SELECT 'REQUEST' AS CATEGORY_CODE, 'PENDING_CHECKER'    AS STATUS_CODE, 'Pending at Checker'    AS STATUS_DISPLAY FROM DUAL UNION ALL
    SELECT 'REQUEST',                  'PENDING_AUTHORIZER',                'Pending at Authorizer'                   FROM DUAL UNION ALL
    SELECT 'REQUEST',                  'AUTHORIZED',                        'Authorized'                              FROM DUAL UNION ALL
    SELECT 'REQUEST',                  'REJECTED',                          'Rejected'                                FROM DUAL
) d
ON (s.CATEGORY_CODE = d.CATEGORY_CODE AND s.STATUS_CODE = d.STATUS_CODE)
WHEN NOT MATCHED THEN
    INSERT (CATEGORY_CODE, STATUS_CODE, STATUS_DISPLAY)
    VALUES (d.CATEGORY_CODE, d.STATUS_CODE, d.STATUS_DISPLAY);

COMMIT;

