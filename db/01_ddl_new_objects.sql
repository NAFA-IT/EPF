-- ── Sequences ────────────────────────────────────────────────
CREATE SEQUENCE EPF_COMPANY_SEQ  START WITH 100 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE EPF_CHG_REQ_SEQ  START WITH 1   INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE EPF_AUTH_GROUP_SEQ START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

-- ── Onboarding submission tracker ────────────────────────────
CREATE TABLE EPF_ONBOARDING_SUBMISSIONS (
    SUBMISSION_ID      NUMBER         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COMPANY_ID         NUMBER         NOT NULL,
    SUBMISSION_REF_NO  VARCHAR2(30)   NOT NULL,
    TAB1_COMPLETE      VARCHAR2(1)    DEFAULT 'N' NOT NULL,
    TAB2_COMPLETE      VARCHAR2(1)    DEFAULT 'N' NOT NULL,
    TAB3_COMPLETE      VARCHAR2(1)    DEFAULT 'N' NOT NULL,
    TAB4_COMPLETE      VARCHAR2(1)    DEFAULT 'N' NOT NULL,
    STATUS_ID          NUMBER         NOT NULL,
    SUBMITTED_BY       NUMBER,
    SUBMITTED_DATE     DATE,
    CHECKER_ID         NUMBER,
    CHECKED_DATE       DATE,
    CHECKER_REMARKS    VARCHAR2(2000),
    CREATED_BY         NUMBER         NOT NULL,
    CREATED_DATE       DATE           DEFAULT SYSDATE NOT NULL,
    UPDATED_BY         NUMBER,
    UPDATED_DATE       DATE,
    CONSTRAINT EPF_ONB_COMP_UK UNIQUE (COMPANY_ID)
);

-- ── Per-section Change Request detail ────────────────────────
CREATE TABLE EPF_CR_SECTION_CHANGES (
    SECTION_CHANGE_ID  NUMBER         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHANGE_REQ_ID      NUMBER         NOT NULL,
    SECTION_CODE       VARCHAR2(50)   NOT NULL,   -- ACCOUNT / SETTINGS / USERS / GROUPS
    SECTION_LABEL      VARCHAR2(100),
    OLD_VALUES_JSON    CLOB,
    NEW_VALUES_JSON    CLOB,
    CHANGE_SUMMARY     VARCHAR2(4000),
    DISPLAY_ORDER      NUMBER         DEFAULT 1,
    CREATED_BY         NUMBER,
    CREATED_DATE       DATE           DEFAULT SYSDATE
);

-- ── DFN API staging table ─────────────────────────────────────
CREATE TABLE EPF_EMP_API_STAGING (
    STAGING_ID         NUMBER         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COMPANY_ID         NUMBER         NOT NULL,
    FOLIO_NUMBER       VARCHAR2(50)   NOT NULL,
    CNIC               VARCHAR2(15),
    FULL_NAME          VARCHAR2(200),
    EMAIL              VARCHAR2(200),
    MOBILE_NO          VARCHAR2(20),
    EMPLOYEE_CODE      VARCHAR2(50),
    FUND_CODES         VARCHAR2(500),   -- comma-separated
    DFN_FOLIO_ID       VARCHAR2(100),
    DFN_INVESTOR_ID    VARCHAR2(100),
    PROCESS_STATUS     VARCHAR2(20)   DEFAULT 'PENDING',  -- PENDING/PROCESSED/ERROR/SKIPPED
    BATCH_DATE         DATE           NOT NULL,
    FETCHED_DATE       DATE           DEFAULT SYSDATE,
    PROCESSED_DATE     DATE,
    PROCESS_MESSAGE    VARCHAR2(4000),
    CONSTRAINT EPF_STAGING_STATUS_CK CHECK (PROCESS_STATUS IN ('PENDING','PROCESSED','ERROR','SKIPPED'))
);

CREATE UNIQUE INDEX EPF_STAGING_FOLIO_DATE_UX
    ON EPF_EMP_API_STAGING (COMPANY_ID, FOLIO_NUMBER, BATCH_DATE);

