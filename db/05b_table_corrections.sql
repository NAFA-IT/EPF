-- EPF_ACTIVITY_LOG: create if not exists (pre-existing AAML audit table)
DECLARE
    v_cnt NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_cnt FROM USER_TABLES WHERE TABLE_NAME = 'EPF_ACTIVITY_LOG';
    IF v_cnt = 0 THEN
        EXECUTE IMMEDIATE '
            CREATE TABLE EPF_ACTIVITY_LOG (
                LOG_ID          NUMBER         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                ENTITY_TYPE     VARCHAR2(50)   NOT NULL,
                ENTITY_ID       NUMBER,
                ACTION_CODE     VARCHAR2(50)   NOT NULL,
                REMARKS         VARCHAR2(2000),
                PERFORMED_BY    NUMBER,
                PERFORMED_DATE  DATE           DEFAULT SYSDATE NOT NULL
            )';
    END IF;
END;
/
