-- ── Seed additional REQUEST workflow statuses ─────────────────
-- PENDING_MAKER: for employee-created loan/withdrawal requests
-- that land at the corporate Maker before entering normal flow.
-- COMPLETED: final state once AAML Checker signs off.
MERGE INTO EPF_STATUSES s
USING (
    SELECT 'REQUEST' AS CATEGORY_CODE, 'PENDING_MAKER' AS STATUS_CODE, 'Pending at Maker' AS STATUS_DISPLAY FROM DUAL UNION ALL
    SELECT 'REQUEST',                  'COMPLETED',                     'Completed'                         FROM DUAL UNION ALL
    SELECT 'REQUEST',                  'PENDING_AAML',                  'Pending at AAML'                   FROM DUAL
) d
ON (s.CATEGORY_CODE = d.CATEGORY_CODE AND s.STATUS_CODE = d.STATUS_CODE)
WHEN NOT MATCHED THEN
    INSERT (CATEGORY_CODE, STATUS_CODE, STATUS_DISPLAY)
    VALUES (d.CATEGORY_CODE, d.STATUS_CODE, d.STATUS_DISPLAY);

COMMIT;

-- ── EPF_AUTHORIZER_DECISIONS ──────────────────────────────────
-- Tracks individual per-authorizer approve/reject decisions.
-- One row per authorizer per request.
-- Multi-authorizer: all authorizers of the company's authorizer
-- group must approve before status becomes AUTHORIZED.
CREATE TABLE EPF_AUTHORIZER_DECISIONS (
    DECISION_ID       NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    REQUEST_TYPE      VARCHAR2(30)    NOT NULL,     -- CONTRIB | LOAN | WITHDRAWAL | LIEN | NOC | LOAN_SETTINGS
    REQUEST_ID        NUMBER          NOT NULL,     -- PK of the relevant request table
    AUTHORIZER_UCID   NUMBER          NOT NULL,     -- FK → EPF_USER_COMPANIES
    DECISION          VARCHAR2(10)    NOT NULL,     -- APPROVE | REJECT
    DECISION_DATE     DATE            DEFAULT SYSDATE NOT NULL,
    REMARKS           VARCHAR2(4000),
    CONSTRAINT EPF_AD_AUTH_FK    FOREIGN KEY (AUTHORIZER_UCID) REFERENCES EPF_USER_COMPANIES (USER_COMPANY_ID),
    CONSTRAINT EPF_AD_TYPE_CK    CHECK (REQUEST_TYPE IN ('CONTRIB','LOAN','WITHDRAWAL','LIEN','NOC','LOAN_SETTINGS')),
    CONSTRAINT EPF_AD_DECISION_CK CHECK (DECISION IN ('APPROVE','REJECT')),
    CONSTRAINT EPF_AD_UK          UNIQUE (REQUEST_TYPE, REQUEST_ID, AUTHORIZER_UCID)
);

COMMENT ON TABLE  EPF_AUTHORIZER_DECISIONS IS 'Per-authorizer approve/reject decisions for multi-authorizer workflow (FSD #336-345).';
COMMENT ON COLUMN EPF_AUTHORIZER_DECISIONS.REQUEST_TYPE IS 'Discriminator: CONTRIB, LOAN, WITHDRAWAL, LIEN, NOC, LOAN_SETTINGS.';
COMMENT ON COLUMN EPF_AUTHORIZER_DECISIONS.REQUEST_ID   IS 'PK in the corresponding request table (e.g. EPF_LOAN_REQUESTS.LOAN_ID).';
COMMENT ON COLUMN EPF_AUTHORIZER_DECISIONS.AUTHORIZER_UCID IS 'USER_COMPANY_ID of the authorizer who made this decision.';

-- ── EPF_AAML_QUEUE ────────────────────────────────────────────
-- After all required authorizers approve a request it is placed
-- here for AAML Transaction Posting (FSD #370-371).
CREATE TABLE EPF_AAML_QUEUE (
    QUEUE_ID        NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    REQUEST_TYPE    VARCHAR2(30)    NOT NULL,
    REQUEST_ID      NUMBER          NOT NULL,
    COMPANY_ID      NUMBER          NOT NULL,
    QUEUED_DATE     DATE            DEFAULT SYSDATE NOT NULL,
    STATUS          VARCHAR2(20)    DEFAULT 'PENDING' NOT NULL,  -- PENDING / PROCESSED
    PROCESSED_DATE  DATE,
    NOTES           VARCHAR2(4000),
    CONSTRAINT EPF_AAML_Q_COMP_FK  FOREIGN KEY (COMPANY_ID) REFERENCES EPF_COMPANIES (COMPANY_ID),
    CONSTRAINT EPF_AAML_Q_TYPE_CK  CHECK (REQUEST_TYPE IN ('CONTRIB','LOAN','WITHDRAWAL','LIEN','NOC')),
    CONSTRAINT EPF_AAML_Q_STAT_CK  CHECK (STATUS IN ('PENDING','PROCESSED'))
);

COMMENT ON TABLE  EPF_AAML_QUEUE IS 'Authorized requests awaiting AAML Maker transaction posting in DFN (FSD #370-371).';
COMMENT ON COLUMN EPF_AAML_QUEUE.STATUS IS 'PENDING = waiting for AAML Maker; PROCESSED = AAML Maker/Checker approved.';

-- ── EPF_REALLOC_REQUESTS ──────────────────────────────────────
-- Employee-submitted portfolio reallocation requests (FSD #356-358, #372).
-- These go directly to DFN via the AAML queue (FSD #372).
CREATE TABLE EPF_REALLOC_REQUESTS (
    REALLOC_ID      NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COMPANY_ID      NUMBER          NOT NULL,
    FOLIO_ID        NUMBER          NOT NULL,
    GROUP_ID        NUMBER          NOT NULL,    -- FK → EPF_REALLOC_GROUPS
    NEW_MM_PCT      NUMBER(5,2)     NOT NULL,
    NEW_DEBT_PCT    NUMBER(5,2)     NOT NULL,
    NEW_EQUITY_PCT  NUMBER(5,2)     NOT NULL,
    REQUEST_DATE    DATE            DEFAULT SYSDATE NOT NULL,
    STATUS          VARCHAR2(20)    DEFAULT 'PENDING' NOT NULL,   -- PENDING / SUBMITTED / FAILED
    SUBMITTED_DATE  DATE,
    DFN_REF         VARCHAR2(100),
    CONSTRAINT EPF_RR_COMP_FK   FOREIGN KEY (COMPANY_ID) REFERENCES EPF_COMPANIES (COMPANY_ID),
    CONSTRAINT EPF_RR_FOLIO_FK  FOREIGN KEY (FOLIO_ID)   REFERENCES EPF_FOLIOS (FOLIO_ID),
    CONSTRAINT EPF_RR_GRP_FK    FOREIGN KEY (GROUP_ID)   REFERENCES EPF_REALLOC_GROUPS (GROUP_ID),
    CONSTRAINT EPF_RR_STAT_CK   CHECK (STATUS IN ('PENDING','SUBMITTED','FAILED')),
    CONSTRAINT EPF_RR_MM_CK     CHECK (NEW_MM_PCT     BETWEEN 0 AND 100),
    CONSTRAINT EPF_RR_DEBT_CK   CHECK (NEW_DEBT_PCT   BETWEEN 0 AND 100),
    CONSTRAINT EPF_RR_EQ_CK     CHECK (NEW_EQUITY_PCT BETWEEN 0 AND 100)
);

COMMENT ON TABLE  EPF_REALLOC_REQUESTS IS 'Employee portfolio reallocation requests; submitted directly to DFN (FSD #356-358, #372).';
COMMENT ON COLUMN EPF_REALLOC_REQUESTS.DFN_REF IS 'Reference number returned by DFN on successful submission.';

-- ── EPF_ACCOUNT_STMT_LOG ──────────────────────────────────────
-- Audit log for employee account statement views/email requests
-- (FSD #351-353).
CREATE TABLE EPF_ACCOUNT_STMT_LOG (
    LOG_ID        NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FOLIO_ID      NUMBER          NOT NULL,
    USER_ID       NUMBER          NOT NULL,
    PERIOD_TYPE   VARCHAR2(20)    NOT NULL,    -- LAST30 / LAST90 / INCEPTION / DATE_RANGE
    DATE_FROM     DATE,
    DATE_TO       DATE,
    REQUEST_DATE  DATE            DEFAULT SYSDATE NOT NULL,
    DELIVERY      VARCHAR2(10)    NOT NULL,    -- VIEW / EMAIL
    CONSTRAINT EPF_ASL_FOLIO_FK  FOREIGN KEY (FOLIO_ID) REFERENCES EPF_FOLIOS (FOLIO_ID),
    CONSTRAINT EPF_ASL_USER_FK   FOREIGN KEY (USER_ID)  REFERENCES EPF_USERS (USER_ID),
    CONSTRAINT EPF_ASL_PERIOD_CK CHECK (PERIOD_TYPE IN ('LAST30','LAST90','INCEPTION','DATE_RANGE')),
    CONSTRAINT EPF_ASL_DELIV_CK  CHECK (DELIVERY IN ('VIEW','EMAIL'))
);

COMMENT ON TABLE  EPF_ACCOUNT_STMT_LOG IS 'Audit log of employee account statement views and email requests (FSD #351-353).';
COMMENT ON COLUMN EPF_ACCOUNT_STMT_LOG.DELIVERY IS 'VIEW = clicked View Now button; EMAIL = clicked Request on Email.';

